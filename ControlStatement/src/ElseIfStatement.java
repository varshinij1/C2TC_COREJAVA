
public class ElseIfStatement {

	public static void main(String[] args) {
		String lang="c++";
		if(lang=="python") {
			System.out.println("lang is"+lang);
		}else if(lang=="java") {
			System.out.println("lang is"+lang);
		} else if(lang=="C") {
			System.out.println("lang is"+lang);
		}else {
			System.out.println(lang);
		}

	}

}
